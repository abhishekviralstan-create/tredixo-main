const renderLegalText = (text) => {
  return text.split("\n").map((line, index) => {
    const cleanLine = line.trim();

    if (!cleanLine) {
      return <div key={index} className="h-4 md:h-5" />;
    }

    const isMainHeading =
      /^\d+\.\s/.test(cleanLine) || cleanLine.startsWith("✅");

    const isSubHeading =
      /^(I\.|II\.|III\.|IV\.|V\.|VI\.)/.test(cleanLine);

    const isBullet = cleanLine.startsWith("•");

    const isContact =
      cleanLine.includes("@tredixo.co.in") ||
      cleanLine.includes("www.tredixo.co.in");

    if (isMainHeading) {
      return (
        <h2
          key={index}
          className="mt-10 first:mt-0 mb-4 text-2xl md:text-3xl font-bold leading-snug text-white border-l-4 border-[#52ff1f] pl-4"
        >
          {cleanLine}
        </h2>
      );
    }

    if (isSubHeading) {
      return (
        <h3
          key={index}
          className="mt-6 mb-2 text-lg md:text-xl font-semibold text-[#52ff1f]"
        >
          {cleanLine}
        </h3>
      );
    }

    if (isBullet) {
      return (
        <p
          key={index}
          className="ml-5 mb-2 text-sm md:text-base leading-8 text-gray-300"
        >
          <span className="text-[#52ff1f] mr-2">•</span>
          {cleanLine.replace("•", "").trim()}
        </p>
      );
    }

    if (isContact) {
      return (
        <p
          key={index}
          className="mb-3 text-sm md:text-base leading-8 text-[#52ff1f]"
        >
          {cleanLine}
        </p>
      );
    }

    return (
      <p
        key={index}
        className="mb-3 text-sm md:text-base leading-8 text-gray-400"
      >
        {cleanLine}
      </p>
    );
  });
};

